function DeviceCard({ name, type, status, lastSync, batteryLevel, icon }: {
  name: string;
  type: string;
  status: 'connected' | 'disconnected' | 'syncing';
  lastSync: string;
  batteryLevel?: number;
  icon: 'watch' | 'phone' | 'scale' | 'monitor';
}) {
  const [isPressed, setIsPressed] = useState(false);

  const getDeviceIcon = () => {
    switch (icon) {
      case 'watch':
        return (
          <svg className="w-[24px] h-[24px]" fill="none" viewBox="0 0 24 24">
            <path d="M12 7v5l3 3M9 2h6v2a7 7 0 1 1 0 14v2H9v-2a7 7 0 1 1 0-14V2z" stroke="#949494" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/>
          </svg>
        );
      case 'phone':
        return (
          <svg className="w-[24px] h-[24px]" fill="none" viewBox="0 0 24 24">
            <rect x="5" y="2" width="14" height="20" rx="2" ry="2" stroke="#949494" strokeWidth="2"/>
            <line x1="12" y1="18" x2="12.01" y2="18" stroke="#949494" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/>
          </svg>
        );
      case 'scale':
        return (
          <svg className="w-[24px] h-[24px]" fill="none" viewBox="0 0 24 24">
            <rect x="3" y="12" width="18" height="8" rx="2" stroke="#949494" strokeWidth="2"/>
            <path d="M12 8v4M8 8h8" stroke="#949494" strokeWidth="2" strokeLinecap="round"/>
          </svg>
        );
      case 'monitor':
        return (
          <svg className="w-[24px] h-[24px]" fill="none" viewBox="0 0 24 24">
            <rect x="2" y="6" width="20" height="10" rx="2" stroke="#949494" strokeWidth="2"/>
            <path d="M12 12h.01M8 12h.01M16 12h.01" stroke="#949494" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"/>
          </svg>
        );
    }
  };

  const getStatusColor = () => {
    switch (status) {
      case 'connected':
        return '#00C851';
      case 'disconnected':
        return '#FF4444';
      case 'syncing':
        return '#FFA726';
    }
  };

  const getStatusText = () => {
    switch (status) {
      case 'connected':
        return 'Connecté';
      case 'disconnected':
        return 'Déconnecté';
      case 'syncing':
        return 'Synchronisation...';
    }
  };

  return (
    <div 
      className={`bg-white rounded-[20px] p-[20px] relative shrink-0 w-full transition-all duration-150 ${isPressed ? 'scale-[0.98] shadow-lg' : 'hover:shadow-md'} cursor-pointer`}
      onMouseDown={() => setIsPressed(true)}
      onMouseUp={() => setIsPressed(false)}
      onMouseLeave={() => setIsPressed(false)}
    >
      <div className="content-stretch flex items-center justify-between relative w-full">
        <div className="content-stretch flex items-center gap-[16px]">
          <div className="flex items-center justify-center w-[48px] h-[48px] bg-[#F8F9FA] rounded-[12px]">
            {getDeviceIcon()}
          </div>
          <div className="content-stretch flex flex-col gap-[4px] items-start justify-start">
            <div className="font-['SF_Pro_Display:Medium',_sans-serif] text-[16px] text-black">
              <p className="leading-[normal]">{name}</p>
            </div>
            <div className="font-['SF_Pro_Display:Regular',_sans-serif] text-[12px] text-[#606060]">
              <p className="leading-[normal]">{type}</p>
            </div>
          </div>
        </div>
        <div className="content-stretch flex flex-col gap-[4px] items-end justify-start">
          <div className="flex items-center gap-[6px]">
            <div 
              className="w-[8px] h-[8px] rounded-full"
              style={{ backgroundColor: getStatusColor() }}
            />
            <div className="font-['SF_Pro_Display:Medium',_sans-serif] text-[12px]" style={{ color: getStatusColor() }}>
              <p className="leading-[normal]">{getStatusText()}</p>
            </div>
          </div>
          <div className="font-['SF_Pro_Display:Regular',_sans-serif] text-[12px] text-[#606060]">
            <p className="leading-[normal]">{lastSync}</p>
          </div>
          {batteryLevel !== undefined && (
            <div className="flex items-center gap-[4px]">
              <svg className="w-[12px] h-[12px]" fill="none" viewBox="0 0 24 24">
                <rect x="2" y="6" width="16" height="12" rx="2" stroke="#606060" strokeWidth="2"/>
                <path d="M22 10v4" stroke="#606060" strokeWidth="2" strokeLinecap="round"/>
                <rect x="4" y="8" width={`${(batteryLevel / 100) * 12}`} height="8" rx="1" fill="#00C851"/>
              </svg>
              <div className="font-['SF_Pro_Display:Regular',_sans-serif] text-[12px] text-[#606060]">
                <p className="leading-[normal]">{batteryLevel}%</p>
              </div>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

